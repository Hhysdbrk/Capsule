package com.dtxw.entity;


public class Unitroom {

  private String name;
  private String location;
  private String roomName;
  private String roomPath;
  private String function1;
  private String function2;


  public String getName() {
    return name;
  }

  public void setName(String name) {
    this.name = name;
  }


  public String getLocation() {
    return location;
  }

  public void setLocation(String location) {
    this.location = location;
  }


  public String getRoomName() {
    return roomName;
  }

  public void setRoomName(String roomName) {
    this.roomName = roomName;
  }


  public String getRoomPath() {
    return roomPath;
  }

  public void setRoomPath(String roomPath) {
    this.roomPath = roomPath;
  }


  public String getFunction1() {
    return function1;
  }

  public void setFunction1(String function1) {
    this.function1 = function1;
  }


  public String getFunction2() {
    return function2;
  }

  public void setFunction2(String function2) {
    this.function2 = function2;
  }

}
